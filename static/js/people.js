$(document).ready(function(){
	$('#all').click(function(){
		$('.advisors').fadeIn();
		$('.mentors').fadeIn();
		$('.founding').fadeIn();
		$('.management').fadeIn();
	});
	$('#advisors-trigger').click(function(){
		$('.mentors').fadeOut();
		$('.founding').fadeOut();
		$('.management').fadeOut();
		$('.advisors').fadeIn();
	})
	$('#mentors-trigger').click(function(){
		$('.advisors').fadeOut();
		$('.team').fadeOut();
		$('.management').fadeOut();
		$('.mentors').fadeIn();
	})
	$('#founding-trigger').click(function(){
		$('.advisors').fadeOut();
		$('.mentors').fadeOut();
		$('.management').fadeOut();
		$('.founding').fadeIn();
	})
	$('#management-trigger').click(function(){
		$('.advisors').fadeOut();
		$('.founding').fadeOut();
		$('.mentors').fadeOut();
		$('.management').fadeIn();
	})
	$('.title a').click(function(){
		$('.title a').removeClass('active');
		$(this).addClass('active');
	})
})
